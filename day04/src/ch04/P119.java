package ch04;

public class P119 {

	public static void main(String[] args) {
		System.out.println("Start ...");
		
		// 1부터 10까지 반복
		// for문
		int i = 1;
		for (i = 1; i <= 10; i++) {
			System.out.println("For ..." + i);
		}
	
		System.out.println("End ... i="+ i);
	
		// while문
		int s = 1;
		while(s <= 10) {
			s++;
			System.out.println("Whileb ..." + s);
			// s++;			
		}
	
	
	
	}

}
